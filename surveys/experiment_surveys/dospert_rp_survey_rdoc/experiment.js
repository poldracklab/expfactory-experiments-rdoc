const createSurveyQuestions = questions => {
  const firstObj = {
    type: "html",
    prompt: "<h2>Indicate <b>how risky you perceive</b> each situation.</h2>",
    name: "Indicate how risky you perceive each situation.",
  };

  var surveyQuestions = [];

  for (let i = 0; i < questions.length; i++) {
    var questionObj = {
      type: "likert",
      likert_scale_values: [
        { value: 1, text: "Not at all risky" },
        { value: 2, text: "Slightly risky" },
        {
          value: 3,
          text: "Somewhat risky",
        },
        { value: 4, text: "Moderately risky" },
        { value: 5, text: "Risky" },
        { value: 6, text: "Very risky" },

        { value: 7, text: "Extremely risky" },
      ],
      prompt: `${questions[i]}`,
      name: `${questions[i]}`,
      required: true,
    };
    surveyQuestions.push(questionObj);
  }

  // this won't chunk the array b/c questions.length, but keeping in case we want to split up questions into different pages
  function chunkArray(array, chunkSize, prependedObj) {
    let result = [];
    for (let i = 0; i < array.length; i += chunkSize) {
      let chunk = array.slice(i, i + chunkSize);
      chunk.unshift(prependedObj); // Add the object to the start of each chunk
      result.push(chunk);
    }
    return result;
  }

  var preparedQuestionArray = chunkArray(
    surveyQuestions,
    questions.length,
    firstObj
  );

  return preparedQuestionArray;
};

